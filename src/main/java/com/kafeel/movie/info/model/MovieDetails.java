package com.kafeel.movie.info.model;

/**
 * @author Kafeelur Rahman T K
 *
 */
public class MovieDetails {
	
	private String posterUrl;
	
	private String movieName;
	
	private String actorsName;
	
	private String language;
	
	private String country;
	
	private String releasedDate;
	
	private String directorName;
	
	private String writersName;
	
	private String movieAwards;
	
	public String getPosterUrl() {
		return posterUrl;
	}
	
	public void setPosterUrl(String posterUrl) {
		this.posterUrl = posterUrl;
	}
	
	public String getMovieName() {
		return movieName;
	}
	
	public void setMovieName(String movieName) {
		this.movieName = movieName;
	}
	
	public String getActorsName() {
		return actorsName;
	}
	
	public void setActorsName(String actorsName) {
		this.actorsName = actorsName;
	}
	
	public String getLanguage() {
		return language;
	}
	
	public void setLanguage(String language) {
		this.language = language;
	}
	
	public String getCountry() {
		return country;
	}
	
	public void setCountry(String country) {
		this.country = country;
	}
	
	public String getReleasedDate() {
		return releasedDate;
	}
	
	public void setReleasedDate(String releasedDate) {
		this.releasedDate = releasedDate;
	}
	
	public String getDirectorName() {
		return directorName;
	}
	
	public void setDirectorName(String directorName) {
		this.directorName = directorName;
	}
	
	public String getWritersName() {
		return writersName;
	}
	
	public void setWritersName(String writersName) {
		this.writersName = writersName;
	}
	
	public String getMovieAwards() {
		return movieAwards;
	}
	
	public void setMovieAwards(String movieAwards) {
		this.movieAwards = movieAwards;
	}
	
}
